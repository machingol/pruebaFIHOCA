DROP TABLE IF EXISTS BRAND;
DROP TABLE IF EXISTS PRICES;

CREATE TABLE BRAND (
    id int auto_increment primary key,
    name	varchar(25)
);

CREATE TABLE PRICES (
	BRAND_ID int,
	PRICE_LIST int auto_increment primary key,
	START_DATE varchar(25),
	END_DATE varchar(25),
	PRODUCT_ID int,
	PRIORITY int,
	PRICE NUMERIC(10,2),
	CURR varchar(25)
);

ALTER TABLE PRICES ADD CONSTRAINT FK_BRAND_ID FOREIGN KEY (BRAND_ID) REFERENCES BRAND(id);
    

